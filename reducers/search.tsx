const initialState = {
  city: '',
  checkIn: '',
  checkOut: '',
  guests: 0,
  priceMin: 0,
  priceMax: 1000,
  bedrooms: 0,
  beds: 0,
  bathrooms: 0,
  airConditioning: false,
  elevatorAccess: false,
  parking: false,
  outdoorSpace: false,
  kidFriendly: false,
  inSuiteLaundry: false,
}

export const searchReducer = (state = initialState, action: SearchAction) => {
  switch (action.type) {
    case "addCity":
      return {
        city: action.payload.search.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case "addCheckIn":
      return {
        city: state.city,
        checkIn: action.payload.search.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case "addCheckOut":
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: action.payload.search.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case "addGuests":
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests + 1,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case "decreaseGuests":
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests - 1,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changePriceMin':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: action.payload.search.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changePriceMax':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: action.payload.search.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'increaseBedrooms':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms + 1,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'decreaseBedrooms':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms - 1,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'increaseBeds':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds + 1,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'decreaseBeds':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds - 1,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'increaseBathrooms':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms + 1,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'decreaseBathrooms':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms - 1,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeAirConditioning':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: action.payload.search.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeElevatorAccess':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: action.payload.search.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeParking':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: action.payload.search.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeOutdoorSpace':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: action.payload.search.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeKidFriendly':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: action.payload.search.kidFriendly,
        inSuiteLaundry: state.inSuiteLaundry,
      }
    case 'changeInSuiteLaundry':
      return {
        city: state.city,
        checkIn: state.checkIn,
        checkOut: state.checkOut,
        guests: state.guests,
        priceMin: state.priceMin,
        priceMax: state.priceMax,
        bedrooms: state.bedrooms,
        beds: state.beds,
        bathrooms: state.bathrooms,
        airConditioning: state.airConditioning,
        elevatorAccess: state.elevatorAccess,
        parking: state.parking,
        outdoorSpace: state.outdoorSpace,
        kidFriendly: state.kidFriendly,
        inSuiteLaundry: action.payload.search.inSuiteLaundry,
      }
    case 'erase':
      return {
        city: '',
        checkIn: '',
        checkOut: '',
        guests: 0,
        priceMin: 0,
        priceMax: 1000,
        bedrooms: 0,
        beds: 0,
        bathrooms: 0,
        airConditioning: false,
        elevatorAccess: false,
        parking: false,
        outdoorSpace: false,
        kidFriendly: false,
        inSuiteLaundry: false,
      }
    default:
      return state
  }
}
